public class OSP_CH2_Operators {
    public static void main(String[] args) {
        int a = 4;
        int b = 5;  // Modulo Operator
        b *=5;
        System.out.println(b);
        System.out.println(b == b); //Comparsion Operator
        System.out.println(10 > 6);

        //Logical Operators

        System.out.println(64>5 && 64<10);  //&& IS evaluated when both are True
        System.out.println(64>5 || 64<10);  //|| Or Operator


    }
}
