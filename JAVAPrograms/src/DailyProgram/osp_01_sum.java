package DailyProgram;

import java.util.Scanner;

public class osp_01_sum {
    // WAP  to Sum 3 Numbers
   // int a,b,c;
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int c = sc.nextInt();

        int result = a+b+c;
        System.out.println("Sum is "+result);
    }



}
